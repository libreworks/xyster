// Call Xyster_Orm_Binder_SetterTest::main() if this source file is executed directly.
if (!defined('PHPUnit_MAIN_METHOD')) {
    define('PHPUnit_MAIN_METHOD', 'Xyster_Orm_Binder_SetterTest::main');
}

require_once dirname(dirname(__FILE__)) . DIRECTORY_SEPARATOR . 'TestSetup.php';
require_once 'PHPUnit/Framework.php';
require_once 'Xyster/Orm/Binder/Setter.php';

/**
 * Test class for Xyster_Orm_Binder_Setter.
 * Generated by PHPUnit on 2008-05-12 at 10:24:29.
 */
class Xyster_Orm_Binder_SetterTest extends Xyster_Orm_TestSetup
{
    /**
     * @var    Xyster_Orm_Binder_Setter
     */
    protected $object;

    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';

        $suite  = new PHPUnit_Framework_TestSuite('Xyster_Orm_Binder_SetterTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Sets up the fixture
     */
    public function setUp()
    {
        $mf = $this->_mockFactory();
        $this->object = new Xyster_Orm_Binder_Setter($mf->getEntityType('MockBug'), 'bugDescription');
    }

    /**
     * Tests the 'set' method
     */
    public function testSet()
    {
        $entity = $this->_getMockEntity();
        $name = 'bugDescription';
        $value = 'This is the new description';
        $this->assertNotEquals($value, $entity->$name);
        $this->object->set($entity, $value);
        $this->assertEquals($value, $entity->$name);
    }
    
    /**
     * Tests an exception will be thrown for the wrong type of entity
     *
     */
    public function testSetInvalid()
    {
        $this->setExpectedException('Xyster_Data_Binder_Exception');
        $this->_mockFactory()->get('MockAccount');
        $this->object->set(new MockAccount(), 'value');
    }
}

// Call Xyster_Orm_Binder_SetterTest::main() if this source file is executed directly.
if (PHPUnit_MAIN_METHOD == 'Xyster_Orm_Binder_SetterTest::main') {
    Xyster_Orm_Binder_SetterTest::main();
}
