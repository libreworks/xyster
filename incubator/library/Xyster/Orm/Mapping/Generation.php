require_once 'Xyster/Enum.php';
/**
 * Enum to specify when properties are generated by the database 
 *
 * @category  Xyster
 * @package   Xyster_Orm
 * @copyright Copyright (c) 2007-2008 Irrational Logic (http://irrationallogic.net)
 * @license   http://www.opensource.org/licenses/bsd-license.php New BSD License
 */
class Xyster_Orm_Mapping_Generation extends Xyster_Enum
{
    const Never = 0;
    const Insert = 1;
    const Always = 2;
    
    /**
     * Properties are never generated in the database
     *
     * @return Xyster_Orm_Mapping_Generation
     */
    static public function Never()
    {
       return Xyster_Enum::_factory();
    }
    
    /**
     * Properties are generated on insert only
     *
     * @return Xyster_Orm_Mapping_Generation
     */
    static public function Insert()
    {
       return Xyster_Enum::_factory();
    }
    
    /**
     * Properties are generated on insert and update
     *
     * @return Xyster_Orm_Mapping_Generation
     */
    static public function Always()
    {
       return Xyster_Enum::_factory();
    }
}