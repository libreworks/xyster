class Xyster_Orm_Runtime_Property_IdentifierTest extends PHPUnit_Framework_TestCase
{
    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';
        $suite  = new PHPUnit_Framework_TestSuite('Xyster_Orm_Runtime_Property_IdentifierTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Tests the basic operation of the class
     */
    public function testBasic()
    {
        $idGen = $this->getMock('Xyster_Orm_Id_Generator_Interface');
        require_once 'Xyster/Orm/Type/String.php';
        $type = new Xyster_Orm_Type_String;
        require_once 'Xyster/Orm/Engine/IdentifierValue.php';
        $unsavedValue = new Xyster_Orm_Engine_IdentifierValue();
        $prop = new Xyster_Orm_Runtime_Property_Identifier('foobar', $type, true, $unsavedValue, $idGen);
        $this->assertEquals('foobar', $prop->getName());
        $this->assertSame($type, $prop->getType());
        $this->assertTrue($prop->hasMapper());
        $this->assertSame($unsavedValue, $prop->getUnsavedValue());
        $this->assertSame($idGen, $prop->getIdentifierGenerator());
        $this->assertFalse($prop->isAssignedByInsert()); // @todo id gen
    }
}

