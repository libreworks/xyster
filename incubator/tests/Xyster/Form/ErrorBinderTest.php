/**
 * Test class for Xyster_Form_ErrorBinder.
 * Generated by PHPUnit on 2008-05-19 at 13:53:40.
 */
class Xyster_Form_ErrorBinderTest extends PHPUnit_Framework_TestCase
{
    /**
     * Runs the test methods of this class.
     *
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';

        $suite  = new PHPUnit_Framework_TestSuite('Xyster_Form_ErrorBinderTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * @todo Implement testBind().
     */
    public function testBind()
    {
        $form = new Zend_Form;
        $form->addElement('Text', 'foo')
            ->addElement('Text', 'bar');
            
        $errors = new Xyster_Validate_Errors;
        $errors->addError('This is definitely invalid', 'foo');
        $errors->addError('Horrible, messed-up, no-good value!', 'bar');
        
        Xyster_Form_ErrorBinder::bind($form, $errors);
        
        $this->assertEquals(array('This is definitely invalid'), $form->getElement('foo')->getMessages());
        $this->assertEquals(array('Horrible, messed-up, no-good value!'), $form->getElement('bar')->getMessages());
    }
}

