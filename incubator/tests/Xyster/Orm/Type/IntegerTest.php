// Call Xyster_Orm_Type_IntegerTest::main() if this source file is executed directly.
if (!defined('PHPUnit_MAIN_METHOD')) {
    define('PHPUnit_MAIN_METHOD', 'Xyster_Orm_Type_IntegerTest::main');
}
require_once dirname(dirname(dirname(dirname(__FILE__)))) . DIRECTORY_SEPARATOR . 'TestHelper.php';
require_once 'Xyster/Orm/Type/Integer.php';
require_once 'Xyster/Db/Statement/Stub.php';

/**
 * Test class for Xyster_Orm_Type_Integer.
 * Generated by PHPUnit on 2008-07-26 at 14:12:22.
 */
class Xyster_Orm_Type_IntegerTest extends PHPUnit_Framework_TestCase
{
    /**
     * @var    Xyster_Orm_Type_Integer
     */
    protected $object;

    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';
        $suite  = new PHPUnit_Framework_TestSuite('Xyster_Orm_Type_IntegerTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Sets up the fixture
     */
    protected function setUp()
    {
        $this->object = new Xyster_Orm_Type_Integer;
    }

    /**
     * Tests the 'get' method
     */
    public function testGet()
    {
        $stmt = new Xyster_Db_Statement_Stub;
        $stmt->columnValues['foo'] = 123;
        $sess = $this->getMock('Xyster_Orm_Session_Interface');
        $this->assertEquals(123, $this->object->get($stmt, array('foo'), 0, $sess));
    }

    /**
     * Tests the 'getDataType' method
     */
    public function testGetDataType()
    {
        $this->assertSame(Xyster_Db_DataType::Integer(), $this->object->getDataType());
    }

    /**
     * Tests the 'getName' method
     */
    public function testGetName()
    {
        $this->assertEquals('integer', $this->object->getName());
    }

    /**
     * Tests the 'getReturnedType' method
     */
    public function testGetReturnedType()
    {
        $type = $this->object->getReturnedType();
        $this->assertType('Xyster_Type', $type);
        $this->assertEquals('integer', $type->getName());
        $this->assertSame($type, $this->object->getReturnedType());
    }

    /**
     * Tests the 'set' method
     */
    public function testSet()
    {
        $stmt = new Xyster_Db_Statement_Stub;
        $sess = $this->getMock('Xyster_Orm_Session_Interface');
        $this->object->set($stmt, 1234, 0, $sess, array(true));
        $this->assertEquals(1234, $stmt->values[0]);
    }
}

// Call Xyster_Orm_Type_IntegerTest::main() if this source file is executed directly.
if (PHPUnit_MAIN_METHOD == 'Xyster_Orm_Type_IntegerTest::main') {
    Xyster_Orm_Type_IntegerTest::main();
}
