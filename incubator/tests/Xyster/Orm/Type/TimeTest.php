/**
 * Test class for Xyster_Orm_Type_Time.
 * Generated by PHPUnit on 2008-07-27 at 15:31:06.
 */
class Xyster_Orm_Type_TimeTest extends PHPUnit_Framework_TestCase
{
    /**
     * @var    Xyster_Orm_Type_Time
     */
    protected $object;

    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';
        $suite  = new PHPUnit_Framework_TestSuite('Xyster_Orm_Type_TimeTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Sets up the fixture
     */
    protected function setUp()
    {
        $this->object = new Xyster_Orm_Type_Time;
    }

    /**
     * Tests the 'compare' method
     */
    public function testCompare()
    {
        $date = new Zend_Date('11:59:45', Zend_Date::TIMES);
        $date2 = new Zend_Date('01:23:45', Zend_Date::TIMES);
        $this->assertEquals(-1, $this->object->compare($date2, $date));
        $this->assertEquals(0, $this->object->compare($date, $date));
        $this->assertEquals(1, $this->object->compare($date, $date2));
    }

    /**
     * Tests the 'deepCopy' method
     */
    public function testDeepCopy()
    {
        $date = new Zend_Date('11:59:45', Zend_Date::TIMES);
        $this->assertEquals($date, $this->object->deepCopy($date));
    }
        
    /**
     * Tests the 'get' method
     */
    public function testGet()
    {
        $date = new Zend_Date('11:59:45', Zend_Date::TIMES);
        $sess = $this->getMock('Xyster_Orm_Session_Interface');
        $this->assertEquals($date, $this->object->get(array('11:59:45'), 0, $sess));
    }

    /**
     * Tests the 'getDataType' method
     */
    public function testGetDataType()
    {
        $this->assertSame(Xyster_Db_DataType::Time(), $this->object->getDataType());
    }

    /**
     * Tests the 'getName' method
     */
    public function testGetName()
    {
        $this->assertEquals('time', $this->object->getName());
    }

    /**
     * Tests the 'getReturnedType' method
     */
    public function testGetReturnedType()
    {
        $type = $this->object->getReturnedType();
        $this->assertType('Xyster_Type', $type);
        $this->assertEquals('Zend_Date', $type->getName());
        $this->assertSame($type, $this->object->getReturnedType());
    }
    
    /**
     * Tests the 'hasResolve' method
     */
    public function testHasResolve()
    {
        $this->assertTrue($this->object->hasResolve());
    }
    
    /**
     * Tests the 'isEqual' method
     *
     */
    public function testIsEqual()
    {
        $a = date('H:i:s');
        $b = new Zend_Date($a, Zend_Date::TIMES);
        $this->assertTrue($this->object->isEqual($a, $b));
        $this->assertFalse($this->object->isEqual($a, '-1 day'));
    }
    
    /**
     * Tests the 'set' method
     */
    public function testSet()
    {
        $stmt = new Xyster_Db_Statement_Stub;
        $sess = $this->getMock('Xyster_Orm_Session_Interface');
        $date = new Zend_Date('11:59:45', Zend_Date::TIMES);
        $this->object->set($stmt, $date, 0, $sess, array(true));
        $this->assertEquals($date->get(Zend_Date::TIMES), $stmt->values[0]);
        
        $this->object->set($stmt, '11:59:45', 0, $sess, array(true));
        $this->assertEquals($date->get(Zend_Date::TIMES), $stmt->values[0]);
    }
}

