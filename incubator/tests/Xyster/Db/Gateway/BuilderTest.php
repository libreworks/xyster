class Xyster_Db_Gateway_BuilderTest extends PHPUnit_Framework_TestCase
{
    /**
     * @var    Xyster_Db_Gateway_Builder
     */
    protected $object;
    
    protected $gateway;
    
    protected $name = 'foobar';
    
    protected $schema = 'nothing';

    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';

        $suite  = new PHPUnit_Framework_TestSuite('Xyster_Db_Gateway_BuilderTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Sets up the fixture
     */
    protected function setUp()
    {
        $this->gateway = new Xyster_Db_Gateway_Stub;
        $this->object = new Xyster_Db_Gateway_BuilderStub($this->gateway, $this->name, $this->schema);
    }

    /**
     * Tests the 'getName' method
     */
    public function testGetName()
    {
        $this->assertEquals($this->name, $this->object->getName());
    }
    
    /**
     * Tests the 'getSchema' method
