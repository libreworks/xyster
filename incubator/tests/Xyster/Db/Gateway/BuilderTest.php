    public function testGetSchema()
    {
        $this->assertEquals($this->schema, $this->object->getSchema());
    }
    
    /**
     * Tests the '_getGateway' method
     *
     */
    public function testGetGateway()
    {
        $this->assertSame($this->gateway, $this->object->getGateway());
    }
}

