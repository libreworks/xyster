// Call Xyster_Db_Gateway_Pdo_SqliteTest::main() if this source file is executed directly.
if (!defined('PHPUnit_MAIN_METHOD')) {
    define('PHPUnit_MAIN_METHOD', 'Xyster_Db_Gateway_Pdo_SqliteTest::main');
}

require_once dirname(dirname(__FILE__)) . DIRECTORY_SEPARATOR . 'TestCommon.php';
require_once 'Xyster/Db/Gateway/Pdo/Sqlite.php';

/**
 * Test class for Xyster_Db_Gateway_Pdo_Sqlite.
 * Generated by PHPUnit on 2008-03-25 at 18:00:34.
 */
class Xyster_Db_Gateway_Pdo_SqliteTest extends Xyster_Db_Gateway_TestCommon
{
    /**
     * @var    Xyster_Db_Gateway_Pdo_Sqlite
     */
    protected $object;

    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';

        $suite  = new PHPUnit_Framework_TestSuite('Xyster_Db_Gateway_Pdo_SqliteTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Gets the configuration for the database adapter
     * 
     * @return array
     */
    public function getConfig()
    {
        return array(
            'dbname' => TESTS_ZEND_DB_ADAPTER_PDO_SQLITE_DATABASE
        );
    }
    
    /**
     * Gets the adapter 
     *
     * @return string
     */
    public function getDriver()
    {
        return 'Pdo_Sqlite';
    }
    
    /**
     * Tests the 'setAdapter' method
     */
    public function testSetAdapter()
    {
        $gateway = new Xyster_Db_Gateway_Pdo_Sqlite;
        $this->assertNull($gateway->getAdapter());
        $gateway->setAdapter($this->_db);
        $this->assertSame($this->_db, $gateway->getAdapter());
    }

   
    /**
     * Adds a primary key to a table
     */
    public function testAddPrimaryOneColumn()
    {
        $builder = $this->object->createTable('forum');
        $builder->addInteger('forum_id')->null(false)
            ->addVarchar('username', 50)
            ->addVarchar('title', 255)
            ->addClob('message')
            ->addTimestamp('created_on')
            ->execute();
        $this->setExpectedException('Xyster_Db_Gateway_Exception');
        $this->object->addPrimary('forum', 'forum_id');
    }
    
    /**
     * Adds a primary key to a table
     */
    public function testAddPrimaryMultiColumn()
    {
        $builder = $this->object->createTable('forum');
        $builder->addInteger('forum_id')->null(false)
            ->addVarchar('username', 50)
            ->addVarchar('title', 255)
            ->addClob('message')
            ->addTimestamp('created_on')
            ->execute();
        $this->setExpectedException('Xyster_Db_Gateway_Exception');
        $this->object->addPrimary('forum', array('forum_id', 'username'));
    }
    
    /**
     * Removes a column from a table
     */
    public function testDropColumn()
    {
        $this->setExpectedException('Xyster_Db_Gateway_Exception');
        $this->object->dropColumn('forum', 'username');
    }
        
    /**
     * Removes a primary key from a table
     */
    public function testDropPrimary()
    {
        $this->setExpectedException('Xyster_Db_Gateway_Exception');
        $this->object->dropPrimary('forum');
    }

    /**
     * Renames a column
     */
    public function testRenameColumn()
    {
        $this->setExpectedException('Xyster_Db_Gateway_Exception');
        $this->object->renameColumn('forum', 'username', 'username2');
    }
    
    /**
     * Sets the default value for a column
     */
    public function testSetDefault()
    {
        $this->setExpectedException('Xyster_Db_Gateway_Exception');
        $this->object->setDefault('forum', 'username', 'value');
    }

    /**
     * Sets the nullability for a column
     */
    public function testSetNull()
    {
        $this->setExpectedException('Xyster_Db_Gateway_Exception');
        $this->object->setNull('forum', 'username');
    }
    
    /**
     * Sets the nullability for a column
     */
    public function testSetType()
    {
        require_once 'Xyster/Db/Gateway/DataType.php';
        $this->setExpectedException('Xyster_Db_Gateway_Exception');
        $this->object->setType('forum', 'username', Xyster_Db_Gateway_DataType::Char());
    }

    /**
     * Sets a unique index for a column
     */
    public function testSetUnique()
    {
        $this->setExpectedException('Xyster_Db_Gateway_Exception');
        $this->object->setUnique('forum', array('username'));
    }
}

