require_once "PHPUnit/Framework/TestCase.php";
require_once "PHPUnit/Framework/TestSuite.php";
require_once dirname(dirname(dirname(dirname(dirname(__FILE__))))) . DIRECTORY_SEPARATOR . 'TestHelper.php';
require_once 'Xyster/Db/Gateway/TableBuilder/ForeignKey.php';
require_once 'Xyster/Db/ReferentialAction.php';

/**
 * Test class for Xyster_Db_Gateway_TableBuilder_ForeignKey.
 * Generated by PHPUnit on 2008-03-18 at 13:00:34.
 */
class Xyster_Db_Gateway_TableBuilder_ForeignKeyTest extends PHPUnit_Framework_TestCase
{
    /**
     * @var    Xyster_Db_Gateway_TableBuilder_ForeignKey
     */
    protected $object;

    protected $columns = array('column1', 'column2');
    
    protected $fcolumns = array('fcolumn1', 'fcolumn2');
    
    protected $table = 'my_foreign_table';
    
    protected $onDelete;
    
    protected $onUpdate;
    
    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';

        $suite  = new PHPUnit_Framework_TestSuite('Xyster_Db_Gateway_TableBuilder_ForeignKeyTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Sets up the fixture
     */
    protected function setUp()
    {
        $this->onDelete = Xyster_Db_ReferentialAction::SetNull();
        $this->onUpdate = Xyster_Db_ReferentialAction::Cascade();
        
    	$this->object = new Xyster_Db_Gateway_TableBuilder_ForeignKey($this->columns,
            $this->table, $this->fcolumns, $this->onDelete, $this->onUpdate);
    }
    
    /**
     * Tests the 'getColumns' method
     */
    public function testGetColumns()
    {
        $this->assertEquals($this->columns, $this->object->getColumns());
    }

    /**
     * Tests teh 'getForeignColumns' method
     */
    public function testGetForeignColumns()
    {
        $this->assertEquals($this->fcolumns, $this->object->getForeignColumns());
    }

    /**
     * Tests the 'getOnDelete' method
     */
    public function testGetOnDelete()
    {
        $this->assertSame($this->onDelete, $this->object->getOnDelete());
    }

    /**
     * Tests the 'getOnUpdate' method
     */
    public function testGetOnUpdate()
    {
        $this->assertSame($this->onUpdate, $this->object->getOnUpdate());
    }

    /**
     * Tests the 'getTable' method 
     */
    public function testGetTable()
    {
        $this->assertEquals($this->table, $this->object->getTable());
    }
}

