// Call Xyster_Db_ForeignKeyTest::main() if this source file is executed directly.
if (!defined('PHPUnit_MAIN_METHOD')) {
    define('PHPUnit_MAIN_METHOD', 'Xyster_Db_ForeignKeyTest::main');
}
require_once dirname(dirname(dirname(__FILE__))) . DIRECTORY_SEPARATOR . 'TestHelper.php';
require_once 'Xyster/Db/ForeignKey.php';
require_once 'Xyster/Db/Table.php';

/**
 * Test class for Xyster_Db_ForeignKey.
 * Generated by PHPUnit on 2008-07-09 at 11:26:16.
 */
class Xyster_Db_ForeignKeyTest extends PHPUnit_Framework_TestCase
{
    /**
     * @var    Xyster_Db_ForeignKey
     */
    protected $object;

    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';
        $suite  = new PHPUnit_Framework_TestSuite('Xyster_Db_ForeignKeyTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Sets up the fixture
     */
    protected function setUp()
    {
        $this->object = new Xyster_Db_ForeignKey;
    }
    
    /**
     * Tests the 'addReferencedColumn' method
     */
    public function testAddReferencedColumn()
    {
        $column = new Xyster_Db_Column('foobar');
        $this->object->addReferencedColumn($column);
        $column2 = new Xyster_Db_Column('FOOBAR');
        $this->object->addReferencedColumn($column2);
        $this->assertEquals(1, count($this->object->getReferencedColumns()));
    }

    /**
     * Tests the 'getOnDelete' and 'setOnDelete' methods
     */
    public function testGetAndSetOnDelete()
    {
        $action = Xyster_Db_ReferentialAction::SetNull();
        $this->assertSame($this->object, $this->object->setOnDelete($action));
        $this->assertSame($action, $this->object->getOnDelete());
    }

    /**
     * Tests the 'getOnUpdate' and 'setOnUpdate' methods
     */
    public function testGetAndSetOnUpdate()
    {
        $action = Xyster_Db_ReferentialAction::Cascade();
        $this->assertSame($this->object, $this->object->setOnUpdate($action));
        $this->assertSame($action, $this->object->getOnUpdate());
    }

    /**
     * Tests the 'getReferencedColumns' method
     */
    public function testGetReferencedColumns()
    {
        $columns = $this->object->getReferencedColumns();
        $this->assertType('array', $columns);
    }

    /**
     * Tests the 'getReferencedTable' and 'setReferencedTable' methods
     */
    public function testGetAndSetReferencedTable()
    {
        $table = new Xyster_Db_Table;
        $this->assertSame($this->object, $this->object->setReferencedTable($table));
        $this->assertSame($table, $this->object->getReferencedTable());
    }
}

// Call Xyster_Db_ForeignKeyTest::main() if this source file is executed directly.
if (PHPUnit_MAIN_METHOD == 'Xyster_Db_ForeignKeyTest::main') {
    Xyster_Db_ForeignKeyTest::main();
}
