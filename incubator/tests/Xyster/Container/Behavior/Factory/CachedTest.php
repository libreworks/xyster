require_once dirname(__FILE__) . '/CommonTest.php';
require_once 'Xyster/Container/Behavior/Factory/Cached.php';

/**
 * Test class for Xyster_Container_Behavior_Factory_Cached.
 * Generated by PHPUnit on 2008-01-06 at 13:33:47.
 */
class Xyster_Container_Behavior_Factory_CachedTest extends Xyster_Container_Behavior_Factory_CommonTest
{
    /**
     * @var Xyster_Container_Behavior_Factory_Cached
     */
    protected $object;

    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';

        $suite  = new PHPUnit_Framework_TestSuite('Xyster_Container_Behavior_Factory_CachedTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Sets up the fixture, for example, opens a network connection.
     * This method is called before a test is executed.
     *
     * @access protected
     */
    protected function setUp()
    {
        parent::setUp();
        $this->properties->merge(Xyster_Container_Features::CACHE());
        $this->object = new Xyster_Container_Behavior_Factory_Cached;
    }
    
    /**
     * Tests the 'addComponentAdapter' method
     */
    public function testAddComponentAdapterNoCache()
    {
        $this->properties->merge(Xyster_Container_Features::NO_CACHE());
        $key = new Xyster_Type('ArrayObject');
        $instance = new ArrayObject;
        $adapter = new Xyster_Container_Adapter_Instance($key, $instance, $this->monitor);
        $return = $this->object->addComponentAdapter($this->monitor, $this->properties, $adapter);
        $this->assertNotType($this->_getInjectorClass(), $return);
        $this->assertEquals($return->getImplementation(), $key);
    }
    
    /**
     * Tests the 'createComponentAdapter' method
     */
    public function testCreateComponentAdapterNoCache()
    {
        $this->properties->merge(Xyster_Container_Features::NO_CACHE());
        $key = new Xyster_Type('ArrayObject');
        $adapter = $this->object->createComponentAdapter($this->monitor, $this->properties, $key, $key, array());
        $this->assertNotType($this->_getInjectorClass(), $adapter);
        $this->assertSame($adapter->getImplementation(), $key);
    }
    
    protected function _getInjectorClass()
    {
        return 'Xyster_Container_Behavior_Cached';
    }
}

