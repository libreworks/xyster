if (!defined('PHPUnit_MAIN_METHOD')) {
    define('PHPUnit_MAIN_METHOD', 'Xyster_Container_ImmutableTest::main');
}

require_once dirname(dirname(dirname(__FILE__))) . DIRECTORY_SEPARATOR . 'TestHelper.php';
require_once 'PHPUnit/Framework.php';
require_once 'Xyster/Container.php';
require_once 'Xyster/Container/Immutable.php';

/**
 * Test class for Xyster_Container_Immutable.
 * Generated by PHPUnit on 2008-01-31 at 17:56:30.
 */
class Xyster_Container_ImmutableTest extends PHPUnit_Framework_TestCase
{
    /**
     * @var    Xyster_Container_Immutable
     */
    protected $object;

    /**
     * @var Xyster_Container_Mutable
     */
    protected $delegate;
    
    /**
     * Runs the test methods of this class.
     */
    public static function main()
    {
        require_once 'PHPUnit/TextUI/TestRunner.php';

        $suite  = new PHPUnit_Framework_TestSuite('Xyster_Container_ImmutableTest');
        $result = PHPUnit_TextUI_TestRunner::run($suite);
    }

    /**
     * Sets up the fixture
     */
    protected function setUp()
    {
    	$this->delegate = new Xyster_Container;
    	$this->delegate->addComponent('SplObjectStorage');
        $this->object = new Xyster_Container_Immutable($this->delegate);
    }

    /**
     * Tests the 'equals' method
     */
    public function testEquals()
    {
        $this->assertTrue($this->object->equals($this->object));
        
        $this->assertTrue($this->object->equals($this->delegate));
        
        $other = new Xyster_Container_Immutable($this->delegate);
        $this->assertTrue($this->object->equals($other));
    }

    /**
     * Tests the 'hashCode' method
     */
    public function testHashCode()
    {
        $this->assertSame(Xyster_Type::hash($this->delegate), $this->object->hashCode());
    }
}

